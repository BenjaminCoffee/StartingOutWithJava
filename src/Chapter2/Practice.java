package chapter2;

public class Practice {
    private String city;
    private String upperCity;
    private String lowerCity;
    private int stringLength;
    private char oneChar;

    //getter/accessor for String field city
    public String getCity() {
        return city;
    }
    //setter.mutator for String field city
    public void setCity(String city) {
        this.city = city;
    }

    //getter/accessor for int field 'stringLength'
    public int getStringLength() {
        return stringLength;
    }
    //setter.mutator for int field 'stringLength'
    public void setStringLength(int stringLength) {
        this.stringLength = stringLength;
    }

    //getter/accessor for char field 'oneChar'
    public char getOneChar() {
        return oneChar;
    }
    //setter/mutator for char field 'oneChar'
    public void setOneChar(char oneChar) {
        this.oneChar = oneChar;
    }

    //getter/accessor for String field 'upperCity'
    public String getUpperCity() {
        return upperCity;
    }
    //setter/mutator for String field 'upperCity'
    public void setUpperCity(String upperCity) {
        this.upperCity = upperCity;
    }

    //getter/accessor for String field 'lowerCity'
    public String getLowerCity() {
        return lowerCity;
    }
    //setter/mutator for String field 'lowerCity'
    public void setLowerCity(String lowerCity) {
        this.lowerCity = lowerCity;
    }
}

